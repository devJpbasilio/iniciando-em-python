nome = "João" # String
idade = 30 # Inteiro
altura = 1.79 #Float

# Entrada e Saída de dados
""" 
    Entrada de dados: Para receber dados do usuário, usamos a função input().
    Para Saída: Para exibir informações, usamos o print().
"""
print(nome, idade, altura)
peso = float(input("Digite seu peso: "))
print(f"{nome} tem {idade} anos de idade, sua altura é {altura} e seu peso é {peso}")
